// Declare two numbers
let num1 = 10; // Example value for num1
let num2 = 5;  // Example value for num2

// Declare the result variable
let result;

// Simple if-else to determine the value of result
if (num1 > num2) {
  result = num1 * 2; // If num1 is bigger, result is double of num1
} else {
  result = num1 + num2; // Otherwise, result is the sum of num1 and num2
}

// Print the result
console.log(result); // Output will be 20 since num1 is bigger than num2
